package org.game.connect4.util;

/**
 * GameConstants stores all the static constants used in the ConnectFour library.
 */
public class GameConstants {
    /**
     * Empty slots in a grid are represented as '.'
     */
    public static final String EMPTY_SLOT = ".";
    /**
     * The default height of a ConnectFour grid is 6
     */
    public static final int DEFAULT_HEIGHT = 6;
    /**
     * The default width of a ConnectFour grid is 7
     */
    public static final int DEFAULT_WIDTH = 7;
    /**
     * The default name of the first computer player is 'Computer1'
     */
    public static final String DEFAULT_COMPUTER1 = "Computer1";
    /**
     * The default name of the second computer player is 'Computer2'
     */
    public static final String DEFAULT_COMPUTER2 = "Computer2";
}

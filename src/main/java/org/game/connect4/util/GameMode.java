package org.game.connect4.util;

/**
 * GameMode enum enumerates the different possible modes in which you can play the ConnectFour game.
 */
public enum GameMode {
    /**
     * Represents the mode in which two humans play against each other
     */
    PLAYER_VS_PLAYER,
    /**
     * Represents the mode in which a humans plays against a computer
     */
    PLAYER_VS_COMPUTER,
    /**
     * Represents the mode in which two computers play against each other
     */
    COMPUTER_VS_COMPUTER
}
